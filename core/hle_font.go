// fontSet converts the 1bpp font data to 2bpp using the given color indices
// and writes 4096 bytes to K2GE character RAM at $A000.
//
// For each 1bpp bit (MSB first):
//   - set bit -> fontColor (2-bit value)
//   - clear bit -> bgColor (2-bit value)
//
// Output format per row: [lo, hi] where hi holds the left 4 pixels
// and lo holds the right 4 pixels, each pixel as a 2-bit value.
func fontSet(mem *Memory, fontColor, bgColor uint8) {
	fc := fontColor & 0x03
	bc := bgColor & 0x03
	dst := uint32(0xA000)

	for i := 0; i < 2048; i++ {
		srcByte := hleFontData[i]

		// Left 4 pixels (bits 7-4) -> hi byte
		var hi uint8
		for px := 0; px < 4; px++ {
			var c uint8
			if srcByte&(0x80>>uint(px)) != 0 {
				c = fc
			} else {
				c = bc
			}
			hi |= c << uint(6-px*2)
		}

		// Right 4 pixels (bits 3-0) -> lo byte
		var lo uint8
		for px := 0; px < 4; px++ {
			var c uint8
			if srcByte&(0x08>>uint(px)) != 0 {
				c = fc
			} else {
				c = bc
			}
			lo |= c << uint(6-px*2)
		}

		mem.Write(tlcs900h.Byte, dst, uint32(lo))
		mem.Write(tlcs900h.Byte, dst+1, uint32(hi))
		dst += 2
	}
}

